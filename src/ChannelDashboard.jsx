  const SKIP_TEAMS = new Set(["전사 합계", "해외 전체", "국내 전체"]);
  const CHANNEL_ITEMS = new Set(["매출", "매출원가", "운반비", "지급수수료", "광고선전비", "판매촉진비", "한계이익", "한계이익률"]);

